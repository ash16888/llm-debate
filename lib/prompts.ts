function getLastOpponentMessage(messages: string[], currentRole: RoleType): string | null {
  if (messages.length === 0) return null;
  
  // –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ä–æ–ª—å –æ–ø–ø–æ–Ω–µ–Ω—Ç–∞
  const opponentRole = currentRole === 'proponent' ? '–ö—Ä–∏—Ç–∏–∫' : '–°—Ç–æ—Ä–æ–Ω–Ω–∏–∫';
  
  // –ò—â–µ–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–ø–ø–æ–Ω–µ–Ω—Ç–∞
  for (let i = messages.length - 1; i >= 0; i--) {
    if (messages[i].includes(opponentRole)) {
      // –ò–∑–≤–ª–µ–∫–∞–µ–º —Ç–æ–ª—å–∫–æ —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è (–ø–æ—Å–ª–µ –ø–µ—Ä–≤–æ–≥–æ –ø–µ—Ä–µ–Ω–æ—Å–∞ —Å—Ç—Ä–æ–∫–∏)
      const lines = messages[i].split('\n');
      return lines.slice(1).join('\n').trim();
    }
  }
  
  return null;
}

