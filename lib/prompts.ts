export function formatMessageHistory(messages: Array<{
  model: string;
  role: string;
  content: string;
  round: number;
}>): string[] {
  return messages.map((msg) => {
    const roleLabel = ROLE_PROMPTS[msg.role as RoleType]?.name || msg.role;
    return `${roleLabel} (${msg.model}, –†–∞—É–Ω–¥ ${msg.round}):\n${msg.content}`;
  });
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å—É–º–º–∞—Ä–∏–∑–∞—Ü–∏–∏ –¥–ª–∏–Ω–Ω—ã—Ö –¥–µ–±–∞—Ç–æ–≤
export function summarizeDebateHistory(
  messages: string[],
  maxMessages: number = 6
): { summary: string[]; keyArguments: { proponent: string[]; critic: string[] } } {
  if (messages.length <= maxMessages) {
    return { 
      summary: messages, 
      keyArguments: { proponent: [], critic: [] } 
    };
  }
  
  // –û—Å—Ç–∞–≤–ª—è–µ–º –ø–µ—Ä–≤—ã–µ 2 –∏ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 4 —Å–æ–æ–±—â–µ–Ω–∏—è
  const summary = [
    ...messages.slice(0, 2),
    `[... ${messages.length - maxMessages} —Å–æ–æ–±—â–µ–Ω–∏–π –ø—Ä–æ–ø—É—â–µ–Ω–æ –¥–ª—è –∫—Ä–∞—Ç–∫–æ—Å—Ç–∏ ...]`,
    ...messages.slice(-4)
  ];
  
  // –ò–∑–≤–ª–µ–∫–∞–µ–º –∫–ª—é—á–µ–≤—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã –∏–∑ –ø—Ä–æ–ø—É—â–µ–Ω–Ω–æ–π —á–∞—Å—Ç–∏
  const keyArguments = extractKeyArguments(messages.slice(2, -4));
  
  return { summary, keyArguments };
}

// –ò–∑–≤–ª–µ—á–µ–Ω–∏–µ –∫–ª—é—á–µ–≤—ã—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∏–∑ –∏—Å—Ç–æ—Ä–∏–∏
function extractKeyArguments(messages: string[]): { proponent: string[]; critic: string[] } {
  const keyArguments = {
    proponent: [] as string[],
    critic: [] as string[]
  };
  
  messages.forEach(msg => {
    // –ü—Ä–æ—Å—Ç–æ–µ –∏–∑–≤–ª–µ—á–µ–Ω–∏–µ –ø–µ—Ä–≤–æ–≥–æ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è –∫–∞–∫ –∫–ª—é—á–µ–≤–æ–≥–æ –∞—Ä–≥—É–º–µ–Ω—Ç–∞
    const firstSentence = msg.split(/[.!?]/)[0]?.trim();
    if (firstSentence) {
      if (msg.includes('–°—Ç–æ—Ä–æ–Ω–Ω–∏–∫')) {
        keyArguments.proponent.push(firstSentence);
      } else if (msg.includes('–ö—Ä–∏—Ç–∏–∫')) {
        keyArguments.critic.push(firstSentence);
      }
    }
  });
  
  // –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã
  keyArguments.proponent = [...new Set(keyArguments.proponent)].slice(0, 3);
  keyArguments.critic = [...new Set(keyArguments.critic)].slice(0, 3);
  
  return keyArguments;
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–¥–µ–ª–µ–Ω–∏—è –≥–ª–∞–≤–Ω—ã—Ö —Ç–µ–∑–∏—Å–æ–≤ –≤ —Å–æ–æ–±—â–µ–Ω–∏–∏
export function highlightKeyPoints(message: string): string {
  // –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è
  const sentences = message.split(/(?<=[.!?])\s+/);
  
  // –ü–æ–º–µ—á–∞–µ–º –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞-–∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã
  const keyIndicators = [
    '–≥–ª–∞–≤–Ω–æ–µ', '–≤–∞–∂–Ω–æ', '–∫–ª—é—á–µ–≤–æ–π', '–æ—Å–Ω–æ–≤–Ω–æ–π', '—Ñ–∞–∫—Ç', 
    '–¥–æ–∫–∞–∑—ã–≤–∞–µ—Ç', '–æ–ø—Ä–æ–≤–µ—Ä–≥–∞–µ—Ç', '—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ', '–ø–æ—Ç–æ–º—É —á—Ç–æ'
  ];
  
  return sentences.map(sentence => {
    const hasKeyIndicator = keyIndicators.some(indicator => 
      sentence.toLowerCase().includes(indicator)
    );
    
    return hasKeyIndicator ? `**${sentence}**` : sentence;
  }).join(' ');
}

